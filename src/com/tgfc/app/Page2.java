package com.tgfc.app;

public class Page2 {
	
	private String title;
	private String url;
	private String replynum;
	
	public Page2(String title, String url, String replynum) {
		this.title = title;
		this.url = url;
		this.replynum = replynum;
	}
	
	public String getTitle() {
		return title;
	}
	
	public String getUrl() {
		return url;
	}
	
	public String getReplynum() {
		return replynum;
	}

}
